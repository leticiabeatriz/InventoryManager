    private void selecionar() {
        modelo.setNumRows(0);
        FornecedorDAO fornecedor = new FornecedorDAO();
        for (Fornecedor f : fornecedor.selecionarFornecedores()) {
            modelo.addRow(new Object[]{"  " + f.getCnpj(), "  " + f.getNome(), "  " + f.getEmail(), "  " + new TelefoneFornecedorDAO().selectTelefonesJuntos(f.getCnpj())});
        }
    }

    private void selecionarComRest() {
        modelo.setNumRows(0);
        FornecedorDAO fornecedor = new FornecedorDAO();
        for (Fornecedor f : fornecedor.selecionarFornecedores(pesquisaFornecedores.getText())) {
            modelo.addRow(new Object[]{"  " + f.getCnpj(), "  " + f.getNome(), "  " + f.getEmail(), "  " + new TelefoneFornecedorDAO().selectTelefonesJuntos(f.getCnpj())});
        }
    }

