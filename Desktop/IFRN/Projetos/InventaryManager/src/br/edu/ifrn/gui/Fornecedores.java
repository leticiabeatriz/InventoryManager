    private void cadastrarFornecedorBtnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarFornecedorBtnCadastrarActionPerformed
        String nome = cadastrarFornecedorNome.getText();
        String cnpj = cadastrarFornecedorCNPJ.getText();
        String email = cadastrarFornecedorEmail.getText();
        String telefone = cadastrarFornecedorTelefone.getText();
        String tetefoneOpcional = cadastrarFornecedorTelefoneOpcional.getText();
        if (nome.isEmpty() || email.isEmpty() || telefone.isEmpty() || cnpj.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, Preencha os campos!");
        } else {
            new FornecedorDAO().cadastrarFornecedor(cnpj, nome, email, telefone, tetefoneOpcional);
        }
        cadastrarFornecedor.dispose();
        selecionar();
    }//GEN-LAST:event_cadastrarFornecedorBtnCadastrarActionPerformed

    private void tableFornecedoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableFornecedoresMouseClicked
        if (evt.getClickCount() != 0) {
            selectedRow = tableFornecedores.getSelectedRow();
            if (selectedRow > 0) {
                this.btnExcluir.setVisible(true);
            }
            if (evt.getClickCount() >= 2 || selectedRow > 0) {
                String cnpj = (String)tableFornecedores.getValueAt(selectedRow, 0);
                new DetalharForncedor(cnpj.trim()).setVisible(true);
                this.dispose();
            }
        }
    }//GEN-LAST:event_tableFornecedoresMouseClicked

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        String cnpj = (String) tableFornecedores.getValueAt(selectedRow, 0);
        cnpj = cnpj.trim();
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int resultado = JOptionPane.showConfirmDialog(null, "Você realmente deseja deletar este fornecedor? TODAS as compre feitas a esse fornecedor também serão excluídas", "AVISO", dialogButton);
        if (resultado == JOptionPane.YES_OPTION) {
            new FornecedorDAO().deletarFornecedor(cnpj);
            selecionar();
        }

        btnExcluir.setVisible(false);
    }//GEN-LAST:event_btnExcluirActionPerformed

